int	ft_atoi(const char *str)
{
	int		i;
	int		sign;
	int		sum;

	i = 0;
	sign = 1;
	sum = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\r'
		|| str[i] == '\n' || str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] && (str[i] >= '0') && (str[i] <= '9'))
		sum = sum * 10 + sign * (int)(str[i++] - '0');
	return (sum);
}
